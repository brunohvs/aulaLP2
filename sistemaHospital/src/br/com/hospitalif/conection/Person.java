package br.com.hospitalif.conection;

public class Person {
	private int idPerson;
	private String name;
	private String cpf;
	private int age;
	private String sanguineType;
	private char sex;
	private String status;
	public int getIdPerson() {
		return idPerson;
	}
	public void setIdPerson(int idPerson) {
		this.idPerson = idPerson;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getSanguineType() {
		return sanguineType;
	}
	public void setSanguineType(String sanguineType) {
		this.sanguineType = sanguineType;
	}
	public char getSex() {
		return sex;
	}
	public void setSex(char sex) {
		this.sex = sex;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	

}
