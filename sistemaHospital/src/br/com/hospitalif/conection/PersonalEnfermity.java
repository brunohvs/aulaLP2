package br.com.hospitalif.conection;

public class PersonalEnfermity {
	private int isEnfermity;
	private String commentary;
	private String enfermityStatus;
	public int getIsEnfermity() {
		return isEnfermity;
	}
	public void setIsEnfermity(int isEnfermity) {
		this.isEnfermity = isEnfermity;
	}
	public String getCommentary() {
		return commentary;
	}
	public void setCommentary(String commentary) {
		this.commentary = commentary;
	}
	public String getEnfermityStatus() {
		return enfermityStatus;
	}
	public void setEnfermityStatus(String enfermityStatus) {
		this.enfermityStatus = enfermityStatus;
	}
	
}
